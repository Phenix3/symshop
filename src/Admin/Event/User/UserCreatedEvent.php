<?php


namespace App\Admin\Event\User;


use App\Entity\User;

class UserCreatedEvent
{
    /**
     * @var User
     */
    private User $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }
}